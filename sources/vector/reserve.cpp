#include "checker.hpp"

int main (void)
{
	NAMESPACE::vector<TYPE>::size_type sz;

	NAMESPACE::vector<TYPE> foo;
	sz = foo.capacity();
	std::cout << "making foo grow:\n";
	for (int i = 0; i < 100; ++i)
	{
		foo.push_back(i);
		if (sz != foo.capacity())
		{
			sz = foo.capacity();
			std::cout << "capacity changed: " << sz << std::endl;
		}
	}

	NAMESPACE::vector<TYPE> bar;
	sz = bar.capacity();
	bar.reserve(100);   // this is the only difference with foo above
	std::cout << "making bar grow:\n";
	for (int i = 0; i < 100; ++i)
	{
		bar.push_back(i);
		if (sz != bar.capacity())
		{
			sz = bar.capacity();
			std::cout << "capacity changed: " << sz << std::endl;
		}
	}
	return 0;
}